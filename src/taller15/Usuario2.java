package taller15;

public class Usuario2 {
    private String nombre;
    private String contrasena;

    public Usuario2(String nombre, String contrasena) {
        this.nombre = nombre;
        this.contrasena = contrasena;
    }

    public String getNombre() {
        return nombre;
    }

    public String getContrasena() {
        return contrasena;
    }
}
